/*
2. Write a JavaScript conditional statement to find the sign of product of three numbers. Display an alert box with the specified sign.
Sample numbers : 3, -7, 2
Output : The sign is -
*/

let n1 = 3;
let n2 = -7;
let n3 = 2;

let product = n1 + n2 + n3;
if (product > 0) {
  console.log("The sign is +");
} else {
  console.log("The sign is -");
}
